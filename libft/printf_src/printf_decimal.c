#include "../libft.h"

int	ft_if_decimal(int n)
{
	int	i;
	int	x;

	x = n;
	i = 1;
	if (x < 0 && x != -2147483648)
	{
		i = 2;
		x = -x;
	}
	else if (x == -2147483648)
	{
		ft_putnbr_fd(n, 1);
		return (11);
	}
	while (x > 9)
	{
		x /= 10;
		i++;
	}
	ft_putnbr_fd(n, 1);
	return (i);
}
